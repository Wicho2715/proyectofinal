    private void jbregistrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jbregistrarMouseClicked

        SQLUsuarios modsql = new SQLUsuarios();
        usuarios mod = new usuarios();

        String pass = new String(txtpassword.getPassword());
        String passcon = new String(txtconfirmar.getPassword());

        if (txtusuario.getText().equals("") || pass.equals("")
                || passcon.equals("") || txtnombre.getText().equals("") || txtcorreo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campos vacios,llenar todos los Campos");
        } else {

            if (pass.equals(passcon)) {

                if (modsql.existeusuario(txtusuario.getText()) == 0) {

                    if (modsql.esEmail(txtcorreo.getText())) {

                        String nuevopass = hash.sha1(pass);

                        mod.setUSUARIO(txtusuario.getText());
                        mod.setPASSWORD(nuevopass);
                        mod.setNOMBREU(txtnombre.getText());
                        mod.setCORREO(txtcorreo.getText());
                        mod.setID_TIPO(2);

                        if (modsql.registrar(mod)) {
                            JOptionPane.showMessageDialog(null, "Registro Guardado");
                            limpiar();
                        } else {
                            JOptionPane.showMessageDialog(null, "Error al Guardar");
                            limpiar();
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "el correo electronico no es valido");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "El usuario ya existe");
                    limpiar();
                }
            } else {
                JOptionPane.showMessageDialog(null, "las contraseñas no coinciden");
            }
        }

    }//GEN-LAST:event_jbregistrarMouseClicked

    private void limpiar(){
            txtusuario.setText("");
            txtpassword.setText("");
            txtconfirmar.setText("");
            txtnombre.setText("");
            txtcorreo.setText("");
        }
